#include "libft.h"

char	*ft_strdup(char const *s)
{
	char	*new;
	int		i;

	i = 0;
	new = (char *)malloc(ft_strlen(s) + 1);
	if (new == NULL)
		return (NULL);
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
